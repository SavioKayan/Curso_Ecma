/*Os aritméticos são:
+ = soma
- = subtração
* = multiplicação
/ = divisão inteira
% = resto da divisão
** = exponenciação
*/

/* Os operadores de atribuição sâo:
+= | a += b |a = a + b
-= | a -= b |a = a - b
*= | a *= b |a = a * b
/= | a /= b |a = a / b
**=| a **= b |a = a ** b
%= | a %= b | a = a % b

Os operadores de encremente são:
++ x++ x = x + 1 ou x += 1
-- x-- x = x - 1 ou x -= 1
*/